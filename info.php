<h2 id="baslikyazi">Oyun Hakkında</h2>
	<p>Counter-Strike: Global Offensive (CS:GO), serisinin 12 yıl önce satışa çıktığı zaman öncülük ettiği takım bazlı aksiyon oynanışını genişletiriyor.</p>
                       <p>CS:GO yeni haritalar, karakterler ve silahların yanı sıra klasik CS içeriğinin (de_dust, vb.) güncellenmiş sürümlerini sunuyor. Ek olarak, CS:GO yeni oyun modları, eşleştirme, sıralama listeleri ve daha fazlasını da marka tarihinde ilk kez oyunculara lanse ediyor.</p>
