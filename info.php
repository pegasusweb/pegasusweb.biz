<h2 id="baslikyazi">Tarihçe</h2>
<p>1999 yılında Counter-Strike bir Half-Life modu olarak çıktı. Counter-Strike betadan tam sürüme doğru çıkış yaparken, rekabetçi nişancılık oyunlarını tanımlayacak olan klasik oynanışı ortaya çıkartmış ve oyunu on yıldan fazladır büyük bir bağlılık ile takip eden bir topluluğu ortaya çıkartmıştır.</p>
<p>Her sürümünde – 1.6, Source ve Global Offensive – Counter-Stirke bir oyuncunun yeteneğinin fiilen ölçüm yeri olmuştur. Dünya dört bir köşesinden gelen takımlar, yerel, bölgesel ve uluslararası turnuvalarda becerilerini ve stratejilerini devamlı olarak göstermektedirler.</p>
<p>Profesyonel Counter-Strike oyuncuları uluslararası oyunculuk topluluklarında efsanevi hale gelmişlerdir. Aşağıda, bu oyuncular hakkında daha fazla bilgi edinebilir ve onların nasıl Counter-Strike tarihine etki ettiklerini öğrenebilirsiniz.</p>
